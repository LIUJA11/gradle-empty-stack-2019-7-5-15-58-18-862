public class Person {
    private String name;

    public Person() {
    }

    public Person(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public void makeACall(Mobile mobile,String message){
        mobile.call( message +".from" + this.name);
    }
}
